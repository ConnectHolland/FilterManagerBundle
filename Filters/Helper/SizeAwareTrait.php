namespace ONGR\FilterManagerBundle\Filters\Helper;

/**
 * This trait defines methods for elasticsearch size value aware filters.
 */
trait SizeAwareTrait
{
    /**
     * @var int
     */
    private $size;

    /**
     * @return int
     */
    public function getSize()
    {
        return $this->size;
    }

    /**
     * @param int $size
     */
    public function setSize($size)
    {
        $this->size = $size;
    }
}
